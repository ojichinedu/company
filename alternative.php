 <h4>Real Estate</h4>
 <p>Real estate is yet another effective alternative investment for investors. Investing in plots, houses, and reaping rental yields or commercial assets are some of the direct ways of investing in real estate. Investors, however, can also invest in real estate indirectly through Real Estate Investment Trusts (REITs).</p>
 <br>
 <h4>Collectibles</h4>
 <p> Cars, stamps, artwork, and vintage wine are normally considered mere prestigious souvenirs. However, they are highly valuable assets for investors who are aware of how profitable these collectibles are to invest in. Cars, coins, art, and stamps are asset classes preferred for such tangible alternative investments.</p>
 <br>
 <h4>Private Equity</h4>
 <p>The equities not listed on stock exchanges fall under the private equity label. These are funds that institutional investors or HNWIs directly place in private companies or use in the buyout
 of public companies. The firms, in turn, utilize the capital for their inorganic and organic growth while expanding their footprint, increasing marketing operations, technological advancement, and making strategic acquisitions.</p>
 <br>
 <h4>Venture Capital</h4>
 <p>Venture capital refers to the type of investment where investors spend on equity
 capital in private startups, having exceptional potential for growth. The concept might sound similar to the private equity concept, but it’s not as it invests equity capital into mature companies. Venture capitalists usually invest in seed and early-stage businesses, while some invest at the expansion stage. The investment horizon
 is typically between 3-7 years. The expected return
 rate is quite high, which is a natural outcome owing to the risk quotient associated with the investment.</p>
